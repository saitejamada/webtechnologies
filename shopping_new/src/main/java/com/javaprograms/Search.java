package com.javaprograms;

public class Search {

	int pid;
	String name;
	int price;
	String desc;
	String image;
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	@Override
	public String toString() {
		return "Search [pid=" + pid + ", name=" + name + ", price=" + price + ", desc=" + desc + ", image=" + image
				+ "]";
	}
	
}
